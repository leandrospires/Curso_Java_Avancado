SELECT 	P1.DOCUMENTO,
		P1.NOME,
		P2.DESCRICAO,
		P2.VALOR
  FROM 	PRESTADORES 			P1,
		PROJETOS 				P2,
		PRESTADORES_PROJETOS 	P3
 WHERE 	P1.DOCUMENTO = P3.DOCUMENTO
   AND 	P2.IDPROJETO = P3.IDPROJETO
   
   
SELECT 	P1.DOCUMENTO,
		P1.NOME, COUNT(*) AS QUANTIDADE, 
		SUM(P2.VALOR) AS VALOR_TOTAL
  FROM 	PRESTADORES 			P1,
		PROJETOS 				P2,
		PRESTADORES_PROJETOS 	P3
 WHERE 	P1.DOCUMENTO = P3.DOCUMENTO
   AND 	P2.IDPROJETO = P3.IDPROJETO
  GROUP BY P1.DOCUMENTO, P1.NOME
  
